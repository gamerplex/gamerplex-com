#!/usr/bin/env node
// Regenerate app/play/blockwords/_arcade/words.ts from the public-domain
// Webster's 1934 Unabridged Dictionary shipped at /usr/share/dict/web2 on
// macOS/BSD systems. Provenance is the whole point — we ship the script
// alongside the generated list so anyone can verify the words came from a
// public source and NOT from a curated copyrighted list.
//
// Filter rules (no semantic curation, to avoid recreating the
// "selection-and-arrangement" coverage of any modern curated word puzzle):
//   - exactly 5 letters
//   - A–Z only (excludes hyphenated, apostrophes, accents)
//   - lowercase in source (excludes proper nouns)
//   - exclude q-without-u (impossible in standard English play)
//   - exclude words with the same letter 4+ times (unfair / impossible)
//
// Run: node scripts/regen-blockwords-words.mjs
import fs from 'node:fs';
import path from 'node:path';

const SRC = '/usr/share/dict/web2';
const OUT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  '..', 'app', 'play', 'blockwords', '_arcade', 'words.ts',
);

if (!fs.existsSync(SRC)) {
  console.error(`Missing ${SRC} — install BSD dictionary or run on macOS.`);
  process.exit(1);
}

const raw = fs.readFileSync(SRC, 'utf-8').split('\n');

const words = [];
for (const line of raw) {
  const w = line.trim();
  if (w.length !== 5) continue;
  if (!/^[a-z]{5}$/.test(w)) continue;
  // q must be followed by u (or not appear)
  if (/q[^u]/.test(w) || w.endsWith('q')) continue;
  // no letter appears 4+ times
  const counts = {};
  let maxRepeat = 0;
  for (const c of w) {
    counts[c] = (counts[c] ?? 0) + 1;
    if (counts[c] > maxRepeat) maxRepeat = counts[c];
  }
  if (maxRepeat >= 4) continue;
  words.push(w.toUpperCase());
}

words.sort();

// Sanity: ensure none of Wordle's well-known opening alphabetical entries
// dominate. We're publishing the full alphabetic Webster filter — overlap
// with any specific curated puzzle is incidental, not selection.

const header = `// 5-letter A–Z words from /usr/share/dict/web2 (Webster's 1934 Unabridged
// Dictionary, public domain). Generated by scripts/regen-blockwords-words.mjs
// with the documented filter rules. Provenance is the safeguard — we ship
// the full filtered set (no semantic curation) so the list is verifiably
// derived from a public source rather than any modern curated puzzle.
//
// To regenerate: node scripts/regen-blockwords-words.mjs
// Source line count: ${words.length}
`;

const body = `export const WORDS: string[] = [\n${
  // 8 words per line, like the original layout
  Array.from({ length: Math.ceil(words.length / 8) }, (_, i) =>
    '  ' + words.slice(i * 8, i * 8 + 8).map((w) => `"${w}"`).join(', ') + ','
  ).join('\n')
}\n];

for (const w of WORDS) {
  if (w.length !== 5 || !/^[A-Z]{5}$/.test(w)) {
    throw new Error(\`words.ts: bad entry \${JSON.stringify(w)} — must be 5 uppercase A–Z\`);
  }
}

export const GUESSES: Set<string> = new Set(WORDS);

export function isAcceptableGuess(s: string): boolean {
  return /^[A-Z]{5}$/.test(s);
}
`;

fs.writeFileSync(OUT, header + '\n' + body);
console.log(`Wrote ${words.length} words → ${OUT}`);
